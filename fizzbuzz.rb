def fizzbuzz(num)
 if num % 3 == 0 && num % 5 == 0
   puts "fizzbuzz"
elsif num % 3 == 0
  puts "fizz"
elsif num % 5 == 0
puts "buzz"

  # if the number is divisible by 3 = > fizz 
  # if the number is divisible by 5 => buzz 
  # if the number is divisible by 3 & 5 => fizzbuzz
  
end
end

fizzbuzz (30)